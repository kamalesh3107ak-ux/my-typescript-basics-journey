// 13.trimEnd()
// 14.concat()

// syntax
// 1.toLowerCase() - convert the string to lowercase
// 2.toUpperCase() - convert the string to uppercase
// 3.charAt() - get the character at a specific index
// 4.index() - get the index of a specific character
// 5.substring() - get a substring from a string
// 6.includes() - check if a string contains a specific substring
// 7.startsWith() - check if a string starts with a specific substring
// 8.endsWith() - check if a string ends with a specific substring
// 9.replace() - replace a substring with another substring
// 10.split() - split a string into an array of substrings
// 11.trim() - remove leading and trailing whitespace from a string
// 12.trimStart() - remove leading whitespace from a string
// 13.trimEnd() - remove trailing whitespace from a string
// 14.concat() - concatenate two or more strings

// heading with examples

// 1.toLowerCase()

// example
let a : string = "KAMALESH";
console.log(a.toLowerCase());

// 2.toUpperCase()

// example
let b : string = "kamalesh";
console.log(b.toUpperCase());

// 3.charAt()

// example
let c : string = "kamalesh";
console.log(c.charAt(3));

// 4.index()

// example
let d : string = "kamalesh";
console.log(d.indexOf("a"));

// 5.substring()

// example
let e : string = "kamalesh";
console.log(e.substring(2,5));

// 6.includes()

// example
let f : string = "kamalesh";
console.log(f.includes("a"));

// 7.startsWith()

// example
let g : string = "kamalesh";
console.log(g.startsWith("k"));

// 8.endsWith()

// example
let h : string = "kamalesh";
console.log(h.endsWith("s"));

// 9.replace()

// example
let i : string = "kamalesh";
console.log(i.replace("a","A"));

// 10.split()

// example
let j : string = "kamalesh";
console.log(j.split("s"));

// 11.trim()

// example
let k : string = "     kamalesh       ";
console.log(k.trim());


// 12.trimStart()

// example
let l : string = "     kamalesh       ";
console.log(l.trimStart());

// 13.trimEnd()

// example
let m : string = "     kamalesh       ";
console.log(m.trimEnd());

// 14.concat()

// example
let n : string = "kamalesh";
let o : string = "poovendran";
console.log(n.concat(o));


